package com.meishe.libbase.pop.enums;

/**
 * Description: Popup显示和隐藏状态
 * Popup shows and hides status
 */
public enum PopupStatus {
    /*
    * 显示
    * show
    * */
    Show,
    /*
    * 正在执行显示动画
    * Executing display animation
    * */
    Showing,
    /*
    * 隐藏
    * Dismiss
    * */
    Dismiss,
    /*
    * 正在执行消失动画
    *   Dismissing
    * */
    Dismissing
}
