package com.meishe.libbase.pop.enums;

/**
 * Description: 窗体显示的位置类型
 * The type of location the form displays
 */
public enum PopupType {
    /**
     * Center popup type.
     * 中间显示类型
     */
    Center,
    /**
     * Bottom popup type.
     * 底部弹出的类型
     */
    Bottom,
    /**
     * Attach view popup type.
     * 依附于指定View或者指定Point的类型
     */
    AttachView,
    /**
     * Image viewer popup type.
     * 大图浏览类型
     */
    ImageViewer,
    /**
     * Position popup type.
     * 自由定位弹窗
     */
    Position
}
